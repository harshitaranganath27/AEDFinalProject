    private void submitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBtnActionPerformed

        Network network = (Network) networkComboBox.getSelectedItem();
        Enterprise.enterprseType type = (Enterprise.enterprseType) entTypeComboBox.getSelectedItem();
        if (network == null || type == null) {
            JOptionPane.showMessageDialog(null, "Invalid Input!");
            return;
        }
        String name = nameTextField.getText();
        if (name == null || name.equals("")) {
            JOptionPane.showMessageDialog(null, "Enter name!");
            return;
        }
        Enterprise enterprise = network.getEntDirectory().createAndAddEnterprise(name, type);
        enterprise.setNetwork(network);
        nameTextField.setText("");
        populateTable();
        dB4OUtil.storeSystem(system);
        JOptionPane.showMessageDialog(null, "Enterprise added successfully!");
        DB4OUtil.getInstance().storeSystem(system);
    }//GEN-LAST:event_submitBtnActionPerformed

