    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) entTable.getModel();

        model.setRowCount(0);
        for (Network network : system.getNetworkList()) {
            for (Enterprise enterprise : network.getEntDirectory().getEnterpriseList()) {
                Object[] row = new Object[3];
                row[0] = enterprise; //enterprise.getNetwork();
                row[1] = network;//.getName();
                row[2] = enterprise.getType().getValue();

                model.addRow(row);
            }
        }
    }

    private void populateComboBox() {
        networkComboBox.removeAllItems();
        entTypeComboBox.removeAllItems();

        for (Network network : system.getNetworkList()) {
            networkComboBox.addItem(network);
        }

        for (Enterprise.enterprseType type : Enterprise.enterprseType.values()) {
            entTypeComboBox.addItem(type);
        }

    }

